package Java_Master_Crux_Crouse;

public class Operator_Variable {
	public static void main(String[] args) {

		int a = 10; // the variable a contain the value is equal to 10
		// you need to put semicolon every time in the java when any lines add

		// int a=10.2 gives you the compile time error
		int b = 20;

		int c = a + b;

		System.out.println("The sum is equal to : " + c);
		c = a - b;
		System.out.println("The subtraction is equal to : " + c);
		c = a * b;
		System.out.println("The multiply is equal to : " + c);
		c = b/ a;
		System.out.println("The Division is equal to : " + c);

	}

}
